@ActivityScope
public class WithdrawSchedulePresenter extends BasePresenter<WithdrawScheduleContract.Model, WithdrawScheduleContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public WithdrawSchedulePresenter(WithdrawScheduleContract.Model model, WithdrawScheduleContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }
}
